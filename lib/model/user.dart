class User {
  final int userId;
  final String img;
  final String fullname;
  final String username;
  final String birth;
  final String email;
  final String number;
  final String address;

  User({
    required this.userId,
    required this.img,
    required this.address,
    required this.birth,
    required this.email,
    required this.fullname,
    required this.number,
    required this.username,
  });
}
